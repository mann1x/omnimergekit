def quantize_one(tools: dict, f16_gguf: Path, output_dir: Path,
                 model_name: str, quant: str, imatrix_file: Path = None,
                 threads: int = None, mtp_info: dict = None,
                 mtp_head_min_type: str = MTP_DEFAULT_HEAD_MIN_TYPE) -> Path:
    """Quantize F16 GGUF to a specific quant type. Returns output path.

    When `mtp_info` is set and `quant` is in MTP_HEAD_OVERRIDE_TIERS,
    appends `--tensor-type blk.{mtp_block_idx}.={mtp_head_min_type}` so
    the MTP head stays at a usable precision while the body drops to
    the low-bit tier.
    """
    out_name = f"{model_name}-{quant}.gguf"
    out_path = output_dir / out_name
    if out_path.exists():
        print(f"  {quant}: already exists, skipping", flush=True)
        return out_path

    # Determine the base quant type (= file-base FTYPE arg) for llama-quantize
    if quant in _L_XL_VARIANTS:
        q_type = _L_XL_VARIANTS[quant]
    elif quant.startswith("CD-"):
        # Handles both legacy full-override (CD-Q4_K_M → Q4_K_M) and the
        # mix family (CD-Q4_K_M_L → Q4_K_M, CD-IQ3_XS_h → IQ3_XS).
        q_type = _cd_file_base_ftype(quant)
    else:
        q_type = quant

    cmd = [tools["quantize"]]

    # CD (ContribDynamic) quants: read the per-layer tensor type file, and
    # detect whether ANY tensor is assigned to an IQ*/Q2_K type that needs imatrix.
    #
    # CD path is HARD-FAIL on map issues. Silent fallback to uniform produces
    # a bit-identical duplicate of the plain Q-* quant under a misleading name —
    # exactly the v5/v5-coder regression (HF LFS OIDs proved CD-* == Q-* for
    # all 5 tiers; see memory/project_v5_cd_quants_are_duplicates.md). Three
    # ways the map can be unusable: missing file, empty file, parse failure —
    # all three abort the tier rather than producing a fake CD quant.
    cd_needs_imatrix = False
    tensor_type_file = None
    if quant.startswith("CD-"):
        tensor_type_file = Path(__file__).parent / f"tensor_types_{quant}.txt"
        if not tensor_type_file.exists():
            raise RuntimeError(
                f"CD quant {quant} requested but {tensor_type_file} does not exist. "
                f"Generate it first via `python generate_cd_maps.py "
                f"--imatrix <imatrix.dat> --out-dir {tensor_type_file.parent}`, "
                f"or remove {quant} from the queue with --exclude. Silent fallback "
                f"to uniform would produce a duplicate of {q_type} under a misleading "
                f"name (see memory/project_v5_cd_quants_are_duplicates.md)."
            )
        try:
            ttxt = tensor_type_file.read_text()
        except OSError as exc:
            raise RuntimeError(
                f"CD quant {quant}: failed to read {tensor_type_file}: {exc}"
            ) from exc
        # Empty / no-override map would also produce a plain Q-* quant — refuse.
        # The map must specify at least one `tensor.name=TYPE` line.
        lines_with_assignment = [
            ln for ln in ttxt.splitlines()
            if "=" in ln and not ln.lstrip().startswith("#")
        ]
        if not lines_with_assignment:
            raise RuntimeError(
                f"CD quant {quant}: {tensor_type_file} has no tensor-type assignments "
                f"(found {len(ttxt.splitlines())} lines, 0 with '=' separator). "
                f"An empty map would produce a duplicate of {q_type}."
            )
        # Any IQ* tier or Q2_K requires an imatrix at quantize time
        if "IQ" in ttxt or "Q2_K" in ttxt:
            cd_needs_imatrix = True

    # Add imatrix if available and quant benefits from it
    # (standalone IQ*/UD-IQ* quants, anything with "IQ" in its name, or CD- maps
    #  that assign at least one tensor to an IQ*/Q2_K tier)
    if imatrix_file and (quant in IMATRIX_QUANTS or "IQ" in quant or cd_needs_imatrix):
        cmd.extend(["--imatrix", str(imatrix_file)])

    # _L/_XL variants: keep embed/output at Q8_0 for better quality
    if quant in _L_XL_VARIANTS:
        cmd.extend(["--output-tensor-type", "q8_0", "--token-embedding-type", "q8_0"])

    # CD (ContribDynamic) quants: use per-layer tensor type file
    if tensor_type_file is not None:
        cmd.extend(["--tensor-type-file", str(tensor_type_file)])

    # MTP (Multi-Token Prediction) head precision override.
    # For low-bit body tiers, lift the MTP block to a higher precision so
    # speculative-decoding accept rate stays intact. The imatrix doesn't
    # cover mtp.* (which converts to blk.{N}.*), so sub-Q4 of the head
    # destroys draft quality. See module docstring + llama.cpp PR #20533.
    if (mtp_info and quant in MTP_HEAD_OVERRIDE_TIERS
            and mtp_head_min_type):
        block_idx = mtp_info["mtp_block_idx"]
        cmd.extend([
            "--tensor-type",
            f"blk.{block_idx}.={mtp_head_min_type.lower()}",
        ])
        print(f"  {quant}: MTP head override — blk.{block_idx}.* "
              f"-> {mtp_head_min_type}", flush=True)

    cmd.extend([str(f16_gguf), str(out_path), q_type])

    if threads:
        cmd.append(str(threads))

    t0 = time.time()
    print(f"  {quant}: quantizing...", flush=True)
    run(cmd, desc=f"quantize {quant}", timeout=7200)
    size_gb = out_path.stat().st_size / 1024**3
    elapsed = time.time() - t0

    # Write `.sha256` sidecar next to the GGUF. The sidecar persists past the
    # upload-worker's delete (which only unlinks the .gguf), so:
    #   - later CD-* tiers can verify they're not bit-identical to an earlier
    #     plain Q-* tier whose GGUF is already gone
    #   - the local dir keeps a tamper-evident audit trail of every produced quant
    #   - the sidecars are also uploaded to HF (~70 bytes each; trivial cost)
    digest = _write_sha256_sidecar(out_path)
    print(f"  {quant}: {size_gb:.2f} GB ({elapsed:.0f}s)  sha256={digest[:12]}",
          flush=True)
    return out_path


def _sha256_of(path: Path) -> str:
    """Stream a SHA-256 over the file. Used to validate CD-* is not a clone
    of the plain Q-* counterpart (silent-fallback bug)."""
    import hashlib
    h = hashlib.sha256()
    with open(path, "rb") as f:
        for chunk in iter(lambda: f.read(1 << 20), b""):
            h.update(chunk)
    return h.hexdigest()


def _sha256_sidecar_path(gguf_path: Path) -> Path:
    """`<x>.gguf` → `<x>.gguf.sha256`. Keeps the .gguf suffix in the sidecar
    name so multiple tiers' sidecars are unambiguously associated."""
    return gguf_path.with_name(gguf_path.name + ".sha256")


def _write_sha256_sidecar(gguf_path: Path) -> str:
    """Compute SHA-256 of `gguf_path` and write `<path>.sha256`. Returns the
    hex digest. Sidecar format matches `sha256sum`:
        <64-char-hex>  <basename>\\n
    so `sha256sum -c <sidecar>` verifies the GGUF when present."""
    digest = _sha256_of(gguf_path)
    sidecar = _sha256_sidecar_path(gguf_path)
    sidecar.write_text(f"{digest}  {gguf_path.name}\n")
    return digest


def _read_sha256_sidecar(gguf_path: Path) -> str | None:
    """Return the hex digest stored in `<path>.sha256`, or None if absent /
    malformed. Used to compare a fresh CD-* against a plain Q-* whose GGUF
    has already been deleted by the upload worker but whose sidecar persists."""
    sidecar = _sha256_sidecar_path(gguf_path)
    if not sidecar.exists():
        return None
    try:
        first = sidecar.read_text().splitlines()[0].strip().split()[0]
    except (OSError, IndexError):
        return None
    first = first.lower()
    if len(first) == 64 and all(c in "0123456789abcdef" for c in first):
        return first
    return None


def verify_cd_distinct(cd_path: Path, quant: str, model_name: str,
                       output_dir: Path, hf_repo: str = None) -> None:
    """For a CD-* tier, verify the output bytes are NOT identical to the
    corresponding plain Q-* quant.

    Comparison sources, in priority order — first hit wins:
      1. `.sha256` sidecar for the plain Q-* in `output_dir`. Always present
         when the plain Q-* was built earlier in this run (sidecar survives
         upload-worker GGUF delete).
      2. Plain Q-* GGUF on disk (rare — sidecar would normally be there too).
      3. The LFS OID of the plain Q-* on the HF repo via tree API.

    Raise RuntimeError on identity. On success, print both digests for the
    operator log. If no comparison source is available, print a warning and
    return — the pre-flight map check in `quantize_one()` is the primary
    guard for that case.
    """
    if not quant.startswith("CD-"):
        return
    q_type = quant[3:]   # CD-Q4_K_M -> Q4_K_M
    cd_sha = _read_sha256_sidecar(cd_path) or _sha256_of(cd_path)

    q_local = output_dir / f"{model_name}-{q_type}.gguf"

    # 1) sidecar for the plain Q-* (preferred — survives GGUF delete)
    q_sha = _read_sha256_sidecar(q_local)
    src = "sidecar"

    # 2) live GGUF on disk (rare; sidecar should be there too)
    if q_sha is None and q_local.exists():
        q_sha = _sha256_of(q_local)
        src = "live-gguf"

    # 3) HF tree API for already-published Q-*
    if q_sha is None and hf_repo:
        try:
            import json as _json
            import urllib.request
            url = f"https://huggingface.co/api/models/{hf_repo}/tree/main"
            with urllib.request.urlopen(url, timeout=15) as r:
                tree = _json.loads(r.read())
            q_fname = f"{model_name}-{q_type}.gguf"
            for f in tree:
                if f.get("path") == q_fname:
                    q_sha = (f.get("lfs", {}) or {}).get("oid", "") or None
                    src = "hf-oid"
                    break
        except Exception as exc:
            print(f"  {quant}: HF SHA-compare skipped ({exc})", flush=True)

    if q_sha is None:
        print(f"  {quant}: SHA={cd_sha[:12]}; no {q_type} counterpart found "
              f"(local sidecar/GGUF or HF) — pre-flight map check is primary guard",
              flush=True)
        return

    if q_sha.lower() == cd_sha.lower():
        raise RuntimeError(
            f"CD integrity FAIL: {cd_path.name} is bit-identical to "
            f"{model_name}-{q_type}.gguf (SHA-256 {cd_sha}, source={src}). "
            f"The CD profile did not apply (--tensor-type-file likely ignored "
            f"or no-op). Refusing to upload — fix the map and re-quantize."
        )
    print(f"  {quant}: SHA OK (CD={cd_sha[:12]} ≠ {q_type}={q_sha[:12]}, source={src})",
          flush=True)


# ── Sanity check ─────────────────────────────────────────────

def sanity_check(tools: dict, gguf_path: Path, quant_name: str,
                 port: int = 18099) -> bool:
    """Quick sanity check: load model, ask capital questions, validate JSON answers.
    Uses --fit to auto-adjust GPU offloading to available VRAM."""
    import requests

    server_bin = tools.get("server")
    if not server_bin:
        print(f"  {quant_name}: sanity check skipped (no llama-server)", flush=True)
        return True

    # Start server with --fit, reasoning disabled for clean JSON answers
    proc = subprocess.Popen(
        [server_bin, "-m", str(gguf_path), "--port", str(port),
         "-c", "2048", "--fit", "on", "--no-warmup", "-t", "4",
         "--reasoning-format", "deepseek", "--reasoning-budget", "0"],
        stdout=subprocess.DEVNULL, stderr=subprocess.PIPE,
    )

    # Wait for ready
    ready = False
    for _ in range(120):
        try:
            r = requests.get(f"http://localhost:{port}/health", timeout=2)
            if r.status_code == 200:
                ready = True
                break
        except Exception:
            pass
        time.sleep(1)

    if not ready:
        proc.kill()
        print(f"  {quant_name}: sanity FAIL — server didn't start", flush=True)
        return False

    # Ask questions
    passed = 0
    for prompt, expected in SANITY_QUESTIONS:
        try:
            r = requests.post(
                f"http://localhost:{port}/v1/chat/completions",
                json={"model": "test", "messages": [{"role": "user", "content": prompt}],
                      "max_tokens": 100, "temperature": 0},
                timeout=120,
            )
            msg = r.json()["choices"][0]["message"]
            content = msg.get("content", "") + " " + msg.get("reasoning_content", "")
            # Simple check: is the expected answer anywhere in the response?
            if expected.lower() in content.lower():
                passed += 1
            else:
                print(f"  {quant_name}: sanity: expected '{expected}', got '{content[:100]}'", flush=True)
        except Exception as e:
            print(f"  {quant_name}: sanity error: {e}", flush=True)

    proc.kill()
    proc.wait()

    # Pass if at least 2/3 correct (some models struggle with accented names like Brasília)
    ok = passed >= 2
    print(f"  {quant_name}: sanity {'PASS' if ok else 'FAIL'} ({passed}/{len(SANITY_QUESTIONS)})", flush=True)
    return ok


