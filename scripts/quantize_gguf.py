# ── MTP (Multi-Token Prediction) support ──────────────────────
# Models with an MTP head (e.g. Qwen3.5/3.6, DeepSeek-V3) carry
# `mtp.*` tensors in their safetensors. llama.cpp's convert_hf_to_gguf.py
# (PR #20533 for Qwen3.5/3.6; equivalent for DeepSeek-V3) remaps them
# to `blk.{num_hidden_layers}.*` — one past the body's last block —
# and emits `nextn_predict_layers = N` in the GGUF metadata.
#
# The imatrix typically does NOT cover the MTP block (it only sees body
# layers during calibration). When the body is quantized to a low-bit
# IQ*/Q2_K tier, the head would inherit that tier and the imatrix
# absence destroys speculative-decoding accept rate. The fix: hold the
# MTP head at a higher precision (default Q4_K) via the
# `--tensor-type blk.{N}.=q4_K` per-tensor override of llama-quantize.
# Reference: froggeric/Qwen3.6-27B-MTP-GGUF receipt + llama.cpp PR #20533.
MTP_DEFAULT_HEAD_MIN_TYPE = "Q4_K"
MTP_HEAD_OVERRIDE_TIERS = {
    "IQ1_S", "IQ1_M", "IQ2_XXS", "IQ2_XS", "IQ2_S", "IQ2_M",
    "Q2_K", "IQ3_XXS",
}


def detect_mtp(model_path: Path) -> dict | None:
    """Detect MTP (Multi-Token Prediction) head in a HF safetensors model.

    Returns a dict {mtp_block_idx, mtp_tensor_count, num_hidden_layers,
    mtp_num_hidden_layers} if `mtp.*` tensors are present in the
    safetensors index, else None. The mtp block index equals
    config.num_hidden_layers (convert_hf_to_gguf.py remaps mtp.* to
    blk.{num_hidden_layers}.* — one past the body's last block).
    """
    if not model_path or not model_path.is_dir():
        return None
    import json as _json
    idx_path = model_path / "model.safetensors.index.json"
    cfg_path = model_path / "config.json"
    try:
        weight_map = _json.loads(idx_path.read_text()).get("weight_map", {})
    except (OSError, _json.JSONDecodeError):
        return None
    mtp_keys = [k for k in weight_map if k.startswith("mtp.")]
    if not mtp_keys:
        return None
    try:
        cfg = _json.loads(cfg_path.read_text())
    except (OSError, _json.JSONDecodeError):
        cfg = {}
    # Multimodal Qwen3.6 (Qwen3VLMoE) nests language-model hparams under
    # `text_config`. The MTP head still maps to the text body's last block,
    # so prefer text_config.num_hidden_layers when present.
    tc = cfg.get("text_config") or {}
    n_hidden = (
        cfg.get("num_hidden_layers")
        or tc.get("num_hidden_layers")
    )
    n_mtp = (
        cfg.get("mtp_num_hidden_layers")
        or tc.get("mtp_num_hidden_layers")
    )
    return {
        "mtp_block_idx": n_hidden,
        "mtp_tensor_count": len(mtp_keys),
        "num_hidden_layers": n_hidden,
        "mtp_num_hidden_layers": n_mtp,
    }


def verify_mtp_in_gguf(f16_gguf: Path, mtp_info: dict, tools: dict) -> None:
    """Post-conversion: verify the F16 GGUF actually contains blk.{N}.*
    tensors where N = num_hidden_layers (the MTP block remap target).

    Hard-fail if absent — the user explicitly asked for an MTP-aware
    build, so a silent missing head is worse than aborting. Falls open
    only when llama-gguf is not on the system (CD/quant builds without
    the inspector tool).
    """
    llama_gguf = Path(tools["quantize"]).parent / "llama-gguf"
    if not llama_gguf.exists():
        print("  MTP verify: llama-gguf not found; skipping post-conversion "
              "check (rely on tier-level visibility).", flush=True)
        return
    block_idx = mtp_info["mtp_block_idx"]
    expected_prefix = f"blk.{block_idx}."
    try:
        result = subprocess.run(
            [str(llama_gguf), "r", str(f16_gguf)],
            capture_output=True, text=True, timeout=180, check=False,
        )
    except Exception as exc:  # noqa: BLE001
        print(f"  MTP verify: llama-gguf failed ({exc}); skipping check.",
              flush=True)
        return
    output = (result.stdout or "") + (result.stderr or "")
    if expected_prefix not in output:
        raise RuntimeError(
            f"MTP head detected in source ({mtp_info['mtp_tensor_count']} "
            f"mtp.* tensors, expected blk.{block_idx}.* in GGUF) but "
            f"{f16_gguf.name} does NOT contain {expected_prefix}* — "
            "convert_hf_to_gguf.py likely doesn't have MTP support for "
            "this architecture. Update llama.cpp to a version including "
            "PR #20533 (Qwen3.5/3.6) or the equivalent for your arch."
        )
    mtp_in_gguf = sum(1 for ln in output.splitlines() if expected_prefix in ln)
    print(f"  MTP verify: PASS — {mtp_in_gguf} blk.{block_idx}.* tensor(s) "
          f"in F16 GGUF (source had {mtp_info['mtp_tensor_count']} mtp.*)",
          flush=True)


def _cd_is_mix_family(quant: str) -> bool:
    """True for CD-mix recipes (the CD-*_L and CD-*_h families). These
    require generate_cd_maps_mix.py and use body-only override + file-base
    FTYPE heuristic protection.

    Identification is set-membership against the registered mix recipe lists
    rather than suffix matching, because the legacy full-override family
    contains `CD-Q3_K_L` whose `_L` is the llama.cpp Q*_K_L variant suffix
    (Q3_K with Q8_0 globals), NOT a mix-family marker."""
    return quant in CD_MIX_L_QUANTS or quant in CD_MIX_HYBRID_QUANTS


# Legacy CD-* tiers whose file-base FTYPE is NOT just "strip CD-". For these,
# generate_cd_maps.py FILE_BASE_FTYPE is authoritative; mirror the entries
# here so quantize_one() picks the right -q arg without importing the map gen.
# Most legacy CD-* match strip-prefix (CD-Q4_K_M → Q4_K_M); only add overrides.
LEGACY_CD_FILE_BASE = {
    # CD-IQ4_K_M is a per-layer IQ-slot recipe (TOP=IQ4_NL, MID=IQ4_XS,
    # LOW=IQ3_S); file-base is Q5_K so llama-quantize lays out the global
    # tensors at Q5_K and the body gets overridden by --tensor-type-file.
    "CD-IQ4_K_M": "Q5_K",
}


def _cd_file_base_ftype(quant: str) -> str:
    """Map a CD tier name to the file-base FTYPE arg for llama-quantize.

    Legacy full-override CD-*: strip CD- prefix, the whole rest is the FTYPE
    UNLESS the quant is in LEGACY_CD_FILE_BASE (special-case overrides).
        CD-Q4_K_M     → Q4_K_M    (strip)
        CD-Q3_K_L     → Q3_K_L    (strip)
        CD-IQ4_K_M    → Q5_K      (LEGACY_CD_FILE_BASE override)
    Mix family CD-*_L / CD-*_h: strip CD- prefix AND _L/_h suffix.
        CD-Q4_K_M_L → Q4_K_M
        CD-IQ3_XS_L → IQ3_XS
        CD-Q5_K_M_h → Q5_K_M

    The mix family is identified by set membership (not suffix), since
    legacy `CD-Q3_K_L` ends in `_L` but is full-override.
    """
    assert quant.startswith("CD-"), quant
    if _cd_is_mix_family(quant):
        core = quant[3:]
        if core.endswith("_h"):
            return core[:-len("_h")]
        if core.endswith("_L"):
            return core[:-len("_L")]
    # Legacy full-override: special-case overrides first, else strip CD-.
    if quant in LEGACY_CD_FILE_BASE:
        return LEGACY_CD_FILE_BASE[quant]
    return quant[3:]

