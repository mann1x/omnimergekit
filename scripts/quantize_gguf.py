def _gguf_tag_from_filename(gguf_path: Path) -> str:
    """Best-effort: extract the quant tag suffix from a GGUF filename
    (mirrors the regex in scripts/ollama_push_generic.sh)."""
    import re
    base = gguf_path.stem  # strip .gguf
    parts = base.split("-")
    pat = re.compile(r"^(F16|F32|Q\d+(_[KS01ML]+)?(_[KSMLX01]+)?|IQ\d+(_[A-Z]+)?|CD-Q\d+(_[KS]+)?(_[KSMLX01]+)?)$")
    for n in (3, 2, 1):
        if n > len(parts):
            continue
        candidate = "-".join(parts[-n:])
        if pat.match(candidate):
            return candidate
    return base.split("-")[-1]  # fallback


def check_ollama_preflight(ollama_target: str, allow_private: bool = False) -> None:
    """Pre-flight before quantization when --ollama-target is set.

    HARD-FAIL with an actionable fix message if ANY of:
      1. `ollama` CLI not on PATH
      2. ollama daemon not reachable on 127.0.0.1:11434
      3. push key (id_ed25519) missing from every standard location
      4. target model namespace does not exist on ollama.com
         (public: HTTP 200; private: caller must pass --ollama-private)

    Refuses to spend hours quantizing only to discover the ollama push chain
    is broken. Triggered by the 2026-05-19 v5-coder CD rebuild incident: pod
    37020050 had no `ollama` binary and no key, so every push step printed
    `[Errno 2] No such file or directory: 'ollama'` AFTER each successful
    HF upload — 4 quants' worth of post-build failures we caught only by
    eyeballing the monitor stream.
    """
    errors = []

    if not shutil.which("ollama"):
        errors.append(
            "ollama CLI not installed (no `ollama` on PATH). "
            "Fix: install from https://ollama.com/download, or omit --ollama-target."
        )

    try:
        with urllib.request.urlopen("http://127.0.0.1:11434/api/tags", timeout=5) as r:
            if r.status != 200:
                errors.append(
                    f"ollama daemon on 127.0.0.1:11434 returned HTTP {r.status} on /api/tags "
                    "(expected 200). Fix: restart `ollama serve` (or `systemctl restart ollama`)."
                )
    except (urllib.error.URLError, ConnectionError, OSError) as e:
        errors.append(
            f"ollama daemon not reachable on 127.0.0.1:11434 ({type(e).__name__}: {e}). "
            "Fix: start the daemon — `sudo -u ollama -E HOME=/usr/share/ollama "
            "OLLAMA_MODELS=/usr/share/ollama/.ollama/models ollama serve &` "
            "or `systemctl start ollama`."
        )

    key_candidates = [
        Path("/usr/share/ollama/.ollama/id_ed25519"),
        Path.home() / ".ollama" / "id_ed25519",
        Path("/root/.ollama/id_ed25519"),
    ]
    key_found = next((p for p in key_candidates if p.is_file()), None)
    if not key_found:
        errors.append(
            "ollama push key (id_ed25519) not found. Searched: "
            + ", ".join(str(p) for p in key_candidates)
            + ". Fix: copy the registered push key from solidpc "
            "(`/usr/share/ollama/.ollama/id_ed25519` + `.pub`) to one of these paths, "
            "or generate a new keypair and register the .pub at "
            "https://ollama.com/settings/keys."
        )

    if "/" not in ollama_target:
        errors.append(
            f"--ollama-target {ollama_target!r} must be 'owner/model' "
            "(e.g. mannix/gemma4-98e-v5-coder)."
        )
    elif allow_private:
        print(
            f"  ollama preflight: skipping ollama.com existence check "
            f"(--ollama-private set; target {ollama_target!r} assumed private)",
            flush=True,
        )
    else:
        probe_url = f"https://ollama.com/{ollama_target}"
        try:
            # GET (not HEAD — ollama.com returns 405 Method Not Allowed on HEAD).
            # Read only the first 256 bytes; we only need the status code, not the body.
            req = urllib.request.Request(
                probe_url,
                headers={"User-Agent": "omk-quantize-gguf/1.0"},
            )
            with urllib.request.urlopen(req, timeout=10) as r:
                r.read(256)
                if r.status != 200:
                    errors.append(
                        f"Model {ollama_target} on ollama.com returned HTTP {r.status} "
                        "(expected 200 for a public model). Fix: confirm name; "
                        "if private, pass --ollama-private to skip this check."
                    )
        except urllib.error.HTTPError as e:
            if e.code == 404:
                errors.append(
                    f"Model {ollama_target} returned HTTP 404 on ollama.com "
                    "(not publicly listed). If the model is PRIVATE: pass "
                    "--ollama-private. If it does NOT EXIST yet: create the namespace "
                    f"first (`ollama create {ollama_target}:bootstrap -f Modelfile && "
                    f"ollama push {ollama_target}:bootstrap`), then re-run."
                )
            else:
                errors.append(
                    f"Model {ollama_target} probe failed HTTP {e.code} ({e.reason}). "
                    "Fix: check ollama.com is reachable; pass --ollama-private to skip."
                )
        except urllib.error.URLError as e:
            errors.append(
                f"ollama.com unreachable while probing {ollama_target}: {e}. "
                "Fix: check network; pass --ollama-private to skip the existence check."
            )

    if errors:
        msg = (
            "\n==== OLLAMA PUSH PRE-FLIGHT FAILED ====\n"
            "Refusing to start the quant pipeline because the ollama push chain "
            "would fail post-quantize. Fix the issues below and re-run, or omit "
            "--ollama-target to skip ollama publish.\n\n"
            + "\n".join(f"  - {e}" for e in errors)
            + "\n"
        )
        raise RuntimeError(msg)

    print(
        f"  ollama preflight: OK "
        f"(target={ollama_target}, key={key_found}, daemon=up)",
        flush=True,
    )


def _prestage_ollama_blob(gguf_path: Path, label: str) -> bool:
    """Pre-stage GGUF as ollama blob via hardlink so `ollama create` skips
    the multi-minute "gathering model components" file-copy step.

    Why: on disk-constrained pods, `ollama create FROM /abs/path.gguf` copies
    the entire GGUF into /root/.ollama/models/blobs/sha256-<hash>. For a 20+
    GB tier this needs 20+ GB of free space AND several minutes; if the disk
    is tight the copy stalls at "gathering model components" until the
    daemon's internal timeout, then we waste a 3-attempt retry loop per quant.

    Fix: pre-create the blob path via hardlink (zero disk cost on same fs).
    `ollama create` then HEAD-checks the blob, finds it, and writes only the
    manifest (sub-second). Falls back to symlink across filesystems.

    Returns True if the blob is in place (or already was). False = the caller
    should still proceed (ollama will do its own copy on `create`).
    """
    blob_dir = Path("/root/.ollama/models/blobs")
    if not blob_dir.is_dir():
        return False
    sidecar = _sha256_sidecar_path(gguf_path)
    sha = None
    if sidecar.exists():
        try:
            sha = sidecar.read_text().split()[0].strip().lower()
        except Exception:
            sha = None
    if not sha:
        print(f"{label}: computing sha256 of {gguf_path.name} (no sidecar) ...", flush=True)
        try:
            sha = _sha256_of(gguf_path).lower()
        except Exception as e:
            print(f"{label}: sha256 compute failed ({e}); skipping pre-stage", flush=True)
            return False
    if len(sha) != 64 or any(c not in "0123456789abcdef" for c in sha):
        return False
    blob_path = blob_dir / f"sha256-{sha}"
    if blob_path.exists():
        print(f"{label}: ollama blob already present for sha256-{sha[:12]}...", flush=True)
        return True
    try:
        os.link(str(gguf_path), str(blob_path))
        print(f"{label}: pre-staged ollama blob sha256-{sha[:12]}... (hardlink, 0 disk cost)", flush=True)
        return True
    except OSError as e:
        if getattr(e, "errno", None) == 18:  # EXDEV: cross-device
            try:
                os.symlink(str(gguf_path.resolve()), str(blob_path))
                print(f"{label}: pre-staged ollama blob sha256-{sha[:12]}... (symlink, cross-fs)", flush=True)
                return True
            except OSError as e2:
                print(f"{label}: blob pre-stage failed ({e2}); ollama will copy", flush=True)
                return False
        print(f"{label}: blob pre-stage failed ({e}); ollama will copy", flush=True)
        return False


def push_to_ollama_from_local(gguf_path: Path, ollama_target: str,
                              template_style: str = "auto",
                              log_dir: Path = None,
                              max_attempts: int = 3,
                              retry_sleep: int = 5) -> bool:
    """ollama create from a LOCAL gguf file (no HF re-download), push to
    ollama.com, then rm the local ref and purge blob digests.

    Returns True on success, False on failure (does not raise — upstream
    HF upload + local-file lifecycle should not be blocked by ollama hiccups).

    template_style:
      "auto"   — let ollama auto-detect chat_template from GGUF metadata
      "gemma4" — embed the custom Gemma 4 tools template (mannix/gemma4-98e style)
    """
    tag = _gguf_tag_from_filename(gguf_path)
    target_tag = f"{ollama_target}:{tag}"
    label = f"  ollama[{tag}]"
    log_dir = log_dir or gguf_path.parent

    workdir = log_dir / "_ollama_push"
    workdir.mkdir(parents=True, exist_ok=True)
    mf_path = workdir / f"Modelfile.{tag}"

    # Pre-stage the GGUF blob in ollama's store via hardlink. Skips the
    # multi-minute "gathering model components" copy on `ollama create`.
    # See _prestage_ollama_blob doc for the disk-pressure incident this fixes.
    _prestage_ollama_blob(gguf_path, label)

    # Build Modelfile — FROM must be absolute. A relative path is interpreted
    # by `ollama create` as a registry hostname; e.g. `FROM gguf_output/foo.gguf`
    # triggers `dial tcp: lookup gguf_output on 8.8.8.8:53: no such host`.
    lines = [f"FROM {gguf_path.resolve()}"]
    if template_style in ("gemma4", "gemma4-a4b"):
        # Built-in Gemma 4 Go-native renderer + parser (ollama >= 0.20).
        # Same recipe ollama.com/library/gemma4:* tags use. Handles the full
        # tool-calling template natively; no Jinja2-to-Go translation needed.
        lines.append("RENDERER gemma4")
        lines.append("PARSER gemma4")
        if template_style == "gemma4-a4b":
            # 26B-A4B MoE only: add the custom TEMPLATE that fixes the
            # 2nd-tool-call-turn bug observed on 98e v3/v4. The renderer + parser
            # provide the structural rendering; this TEMPLATE overrides the
            # per-turn assistant/tool format that the renderer would otherwise
            # emit. Mirrors the published mannix/gemma4-98e recipe.
            lines.append('TEMPLATE """' + GEMMA4_A4B_TOOLS_TEMPLATE + '"""')
    mf_path.write_text("\n".join(lines) + "\n")

    def _flush_local_refs():
        # rm both the target tag and any leftover hf.co/ ref from prior runs
        subprocess.run(["ollama", "rm", target_tag],
                       capture_output=True, text=True)
        # purge any partial blobs
        import glob
        for partial in glob.glob("/root/.ollama/models/blobs/sha256-*-partial*"):
            try:
                os.remove(partial)
            except OSError:
                pass

    create_ok = False
    for attempt in range(1, max_attempts + 1):
        print(f"{label}: ollama create {target_tag} (attempt {attempt}/{max_attempts}) ...", flush=True)
        r = subprocess.run(["ollama", "create", target_tag, "-f", str(mf_path)],
                           capture_output=True, text=True)
        if r.returncode == 0:
            create_ok = True
            break
        print(f"{label}:   create FAILED: {r.stderr.strip()[:300]}", flush=True)
        _flush_local_refs()
        if attempt < max_attempts:
            time.sleep(retry_sleep)
    if not create_ok:
        print(f"{label}: FINAL FAIL on create after {max_attempts} attempts", flush=True)
        return False

    # Push
    print(f"{label}: pushing {target_tag} ...", flush=True)
    push = subprocess.run(["ollama", "push", target_tag],
                          capture_output=True, text=True)
    if push.returncode != 0:
        print(f"{label}: push FAILED: {push.stderr.strip()[:300]}", flush=True)
        # don't return early — still try to clean up
        success = False
    else:
        print(f"{label}: pushed OK", flush=True)
        success = True

    # Capture blob digests from the manifest BEFORE removing the ref
    blobs = set()
    mf_disk = Path(f"/root/.ollama/models/manifests/registry.ollama.ai/{ollama_target}/{tag}")
    if mf_disk.exists():
        import re
        blobs = set(re.findall(r"sha256:[0-9a-f]{64}", mf_disk.read_text()))

    # Remove local ref + force-purge blobs (belt + suspenders against ollama GC bugs)
    subprocess.run(["ollama", "rm", target_tag], capture_output=True, text=True)
    for b in blobs:
        fn = Path("/root/.ollama/models/blobs") / b.replace("sha256:", "sha256-")
        for p in (fn, Path(str(fn) + "-partial")):
            try:
                p.unlink()
            except (OSError, FileNotFoundError):
                pass

    return success


def upload_worker(upload_queue: Queue, repo_id: str, stop_event: Event,
                  keep_base: set[str] = None,
                  ollama_target: str = None,
                  ollama_template: str = "auto",
                  ollama_failed: list = None):
    """Worker thread: uploads completed quants from queue, optionally pushes
    to ollama.com, then deletes the local file.

    keep_base: set of filenames to NOT delete (e.g. the F16 base GGUF).
    ollama_target: if set, ollama-create + push each successfully HF-uploaded
                   quant before deleting the local file. Skips F16/F32 bases.
    ollama_template: "auto" (GGUF metadata) or "gemma4" (custom tools template).
    ollama_failed: shared list to record (quant_name, gguf_filename) pairs whose
                   inline ollama push failed; main() retries them at the end
                   by re-downloading from the published HF GGUF repo.
    """
    while not stop_event.is_set() or not upload_queue.empty():
        try:
            item = upload_queue.get(timeout=5)
        except Exception:
            continue
        if item is None:
            break
        gguf_path, quant_name = item
        try:
            upload_gguf(repo_id, gguf_path, quant_name)
            # Update README with quant size
            size_gb = gguf_path.stat().st_size / 1024**3 if gguf_path.exists() else 0
            update_readme_quant_status(repo_id, quant_name, size_gb, "done")

            # Upload the matching .sha256 sidecar (~70 bytes) for audit trail.
            # Lets anyone independently verify the GGUF bytes via `sha256sum -c`
            # without a full re-download (they can compare against LFS OID),
            # and lets future quantize_gguf.py runs detect CD/Q-name conflicts.
            sidecar = _sha256_sidecar_path(gguf_path)
            if sidecar.exists():
                try:
                    from huggingface_hub import HfApi
                    HfApi().upload_file(
                        path_or_fileobj=str(sidecar),
                        path_in_repo=sidecar.name,
                        repo_id=repo_id,
                        repo_type="model",
                        commit_message=f"Add {sidecar.name} (sha256 sidecar)",
                    )
                except Exception as exc:
                    print(f"  {quant_name}: sidecar upload skipped ({exc})", flush=True)

            # Optional: push to ollama.com from the SAME local file (no re-download).
            # Skip the F16/F32 base (no one runs an F16 GGUF on ollama).
            if (ollama_target and gguf_path.exists()
                    and not quant_name.upper().startswith(("F16", "F32"))):
                pushed = push_to_ollama_from_local(
                    gguf_path, ollama_target,
                    template_style=ollama_template,
                    log_dir=gguf_path.parent,
                )
                if not pushed and ollama_failed is not None:
                    # Record for end-of-script retry (downloads from HF, since
                    # the local file is about to be deleted to save disk).
                    ollama_failed.append((quant_name, gguf_path.name))

            # Delete the GGUF after successful upload to save disk (unless
            # keep_base=None = keep all). The `.sha256` sidecar is deliberately
            # NOT deleted — it stays for cross-tier CD integrity checks (CD-*
            # tiers built later in the same run consult the sidecar of the
            # already-deleted plain Q-* counterpart) and for forensics.
            if keep_base is not None and gguf_path.name not in keep_base:
                gguf_path.unlink(missing_ok=True)
                print(f"  {quant_name}: deleted local file after upload "
                      f"(sidecar {sidecar.name} kept)", flush=True)
        except Exception as e:
            print(f"  {quant_name}: upload FAILED (keeping local file): {e}", flush=True)
        upload_queue.task_done()


